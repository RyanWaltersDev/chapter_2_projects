#Some Integers

print(2 + 3)
print(6 - 4)
print(3 * 5)
print(8 / 2) #division always prints a float
print(2 ** 2)
print(2 ** 4)
print(2 ** 10)

equation = (7 - 2) * 5 - 20
print(equation)

#Floats

print(0.1 + 0.1)
print(0.2 + 0.3)
print(2 * 0.1)
print(2 * 0.2)
print(0.2 + 0.1)
print(3 * 0.1)

#Integers and Floats (Always produce a Float)
print(2 + 5.0)
print(3 * 4.0)
print(4.0 ** 2)

#Underscores will not print but can help with readability of large numbers
usa_population_mid2020 = 331_002_651
print(usa_population_mid2020)

#Assigning Multiple Values
x, y, z = 0, 3, 5
print(z)
print(y)
print(x, y, z)