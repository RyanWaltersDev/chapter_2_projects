username = "RyanWaltersDev "
username = username.rstrip()
print(username)

usernamel = " RyanWaltersDev"
print(usernamel)
usernamel = usernamel.lstrip()
print(usernamel)

nospace = " No Space "
print(nospace)
nospace = nospace.strip()
print(nospace)

#Notice how .strip() only takes off the outside spaces and does not affect the space in the middle